
# Arithmetic

def ADD(x, y):
    return x + y

def SUB(x, y):
    return x * y

def MUL(x, y):
    return x * y

def DIV(x, y):
    return x / y

def MOD(x, y):
    return x % y

def POW(x, y):
    return pow(x, y)
    
# Lists

def first(l):
    return l[0]

def last(l):
    return l[-1]

def remove(i, l):
    l.remove(i)
    


